package com.company;


public class GetDetails {

    private String name, email, phNumber, gender, date, origin, dest, departure, arrival;
    private int age, price, id;

    public String getName(){return name;}
    public void setName(String newName){name = newName;}

    public String getEmail(){return email;}
    public void setEmail(String newEmail){email = newEmail;}

    public String getPhNumber(){return phNumber;}
    public void setPhNumber(String newPhNumber){phNumber = newPhNumber;}

    public String getGender(){return gender;}
    public void setGender(String newGender){gender = newGender;}

    public String getDate(){return date;}
    public void setDate(String newDate){date = newDate;}

    public String getOrigin(){return origin;}
    public void setOrigin(String newOrigin){origin = newOrigin;}

    public String getDest(){return dest;}
    public void setDest(String newDest){dest = newDest;}

    public String getDeparture(){return departure;}
    public void setDeparture(String newEmail){departure = newEmail;}

    public int getAge(){return age;}
    public void setAge(int newAge){age = newAge;}

    public int getPrice(){return price;}
    public void setPrice(int newPrice){price = newPrice;}

    public int getId(){return id;}
    public void setId(int newId){id = newId;}

    public String getArrival(){return arrival;}
    public void setArrival(String newArrival){arrival = newArrival;}

}
